<h4>Design Skeumórfico</h4>
<p>Skeumorfismo: do grego skeuos (recipiente ou ferramenta) e morphe (forma), é a palavra que se refere aos objetos criados a partir de outros elementos. Antes usada apenas com objetos físicos, começou a ser utilizada no meio digital para se referir aos elementos gráficos que utilizavam recursos visuais do mundo real. Vide a metáfora de pastas nos sistemas operacionais, que não somente se parecem com pastas físicas, como também se comportam como pastas, onde podemos guardar o que quiser, inclusive outras pastas.</p>
<p>Os sistemas operacionais imitaram objetos do mundo real, como pastas ou uma lixeira, com a ideia de minimizar a curva de aprendizado dos usuários, afinal nem todos eles sabiam exatamente o que era e como se interagia com um computador. No começo, os computadores eram objetos de nerds e de técnicos de grandes empresas, operando com uma série de comandos. Mas tudo mudou com o surgimento da primeira interface gráfica e todas as suas metáforas na iconografia e em outros elementos. Aliás, essa é uma história que (para quem não a conhece) pode ser conferida no filme Pirates of Silicon Valley.</p>

<p>Esse apelo não é por acaso. Imagine se você utilizasse pela primeira vez um sistema operacional em que não encontrasse nenhum elemento familiar para lhe ajudar a compreender o funcionamento. Provavelmente levaria muito mais tempo para você aprender a utilizar a nova interface.</p>

<p>Apesar do Windows ter feito bastante uso do Skeumorfismo, foi a Apple quem melhor explorou esse conceito para facilitar a assimilação de suas aplicações. Basta darmos uma boa olhada no Mac OS ou iOS: os ícones para o Calendário, Agenda de contatos e outros aplicativos se parecem com os seus correspondentes do mundo físico.</p>

<p>Quando observamos a interface do iBooks, estamos certos de estarmos diante de uma estante de livros. Apesar dos livros estarem com a capa virada para frente – pouco provável em uma estante física – é muito clara a idéia de uma estante de livros representada no mundo digital.</p>

<p>Mas então fica a pergunta: por que tantos profissionais e empresas estão abandonando o Skeumorfismo e aceitando a tendência do Flat Design?</p>s

<h5>Surgimento do Flat Design</h5>

<p>Aparentemente o Flat Design segue uma linha mais simples e minimalista, mas não é só isso. A Microsoft apostou no uso do Flat Design para o Windows Phone e Windows 8. O primeiro parece ter sido um sucesso, mas o segundo não foi tão bem aceito.</p>

<p>A causa disso aparentemente está ligada a curva de aprendizado do usuário. O Flat Design exige uma curva de aprendizado maior apenas quando é mal-empregado. No caso do Windows Phone, temos um usuário consciente de que encontraria um novo sistema em suas mãos, e está ansioso e paciente para explora-lo. Já no caso do Windows 8, temos uma porção de usuários tradicionais que tiveram que reaprender a usar um sistema que já utilizam a anos, e logicamente estranharam. Eu mesmo me aventurei a experimentar, e confesso que mesmo sendo hard user/early adopter de sistemas operacionais e apps, me senti um ignorante ao usar a interface. Suas interações fogem as convenções conhecidas, como a interação de arrastar uma janela para baixo para fechá-la. Reutilizar os elementos de interface do sistema mobile no desktop acabou sendo precipitado, e a Microsoft terá que fazer seu downgrade de interface para agradar seus usuários.</p>

<p>Muita gente culpou o Flat Design. Mas a verdade é que se trata de uma interface complexa com novos conceitos de interação, e o uso do Flat Design apenas aumentou o nível de dificuldade e a curva de aprendizado.</p>

<p>Mas existem sim benefícios no uso do Flat Design. Antes utilizávamos o Skeumorfismo e suas metáforas para representar elementos e facilitar o entendimento no mundo digital. Hoje no entanto o usuário já possui repertório suficiente de elementos do meio digital, e alguns novos conceitos de interface já são bem conhecidos dos usuários, como os diversos movimentos do touchscreen. Por outro lado, muitos novos usuários nunca sequer tiveram contato com alguns elementos do mundo real utilizados em metáforas de interface, como por exemplo o disquete.</p>

<p>Não faz nem uma década que o mundo físico influenciava consideravelmente as interfaces digitais. Mas hoje, com uma geração que já nasceu utilizando o mouse ao invés do chocalho, os elementos do meio digital é que têm influenciado cada vez mais as mídias tradicionais e o contexto social das pessoas.</p>

<p>Por esse e por outros motivos é que o Flat Design ganhará cada vez mais espaço, pois traz novos paradigmas para uma nova geração.</p>
